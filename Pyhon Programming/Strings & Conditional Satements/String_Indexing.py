str = "This is a string"
print(str[0])  # This is a string

# Strings are immutable, so we cannot change the value of a string
# Strings Start with index 0 and end with index n-1, where n is the length of the string

# one more example 

str1 = "Hello World"

print(str1[0:4]) # This will print the characters from index 0 to index 3 (4-1)
print(str1[6:11]) # This will print the characters from index 6 to index 10 (11-1)