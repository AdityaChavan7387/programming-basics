
# In This we will look into some of the string functions in python

str = "Hello World"

str.endswith("ld") # This will return true as the string ends with "ld"

str.startswith("He") # This will return true as the string starts with "He"

str.find("o") # This will return the index of the first occurrence of "o" in the string, which is 4

str.count("o") # This will return the number of occurrences of "o" in the string, which is 2    

str.capitalize() # This will return the string with the first letter capitalized, which is "Hello world"

str.replace("o", "a") # This will return the string with all occurrences of "o" replaced with "a", which is "Hella Warld"

