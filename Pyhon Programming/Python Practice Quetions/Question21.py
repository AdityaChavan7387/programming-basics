# Print numbers from 100 to 1 using a while loop.
Number = 100 # Initialize Number to 100.

while Number >= 1: # Loop will run as long as Number is greater than or equal to 1.
    print(Number)
    Number -= 1 # Decrement Number by 1 to avoid infinite loop.