
# Now we will see how set works in python. A set is a collection which is unordered, unchangeable*, and unindexed. In Python sets are written with curly brackets.

# Create a set
my_set = {"apple", "banana", "cherry"}
print(my_set)

print(type(my_set))
print(len(my_set))


my_set = set() # This is a simple syntax to create an empty set. Note that we cannot use empty curly brackets to create an empty set, as it will create an empty dictionary instead.
print(my_set)