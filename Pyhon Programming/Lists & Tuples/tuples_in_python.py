tup = (20, 30, 40, 50) # creating a tuple. The tuples are immutable, and are used in () brackets. The tuples can contain any type of data, and can be nested.
print(tup)
print(type(tup))
# Accessing the elements of a tuple
print(tup[0]) # accessing the first element of the tuple
print(tup[1]) # accessing the second element of the tuple

