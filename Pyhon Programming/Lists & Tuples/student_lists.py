# Creating A Student lists with Name, Marks, Age, And Grade
student1 = ["John Doe", 85, 20, "A"]
print(student1) # Output: ['John Doe', 85, 20, 'A']

print(student1[0]) # Output: John Doe

student1[0] = "Sam Smith" # Changing the name of the student
print(student1) # Output: ['Sam Smith', 85, 20, 'A'] with updated name