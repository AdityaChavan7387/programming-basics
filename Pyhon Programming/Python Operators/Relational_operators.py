# Relational Operators in Python

A = 50 

B = 20 

print(A == B) # Answer Output will Be False
print(A != B) # Answer Output will Be True

print(A > B)  # Answer Output will Be True
print(A < B)  # Answer Output will Be False

print(A >= B) # Answer Output will Be True
print(A <= B) # Answer Output will Be False